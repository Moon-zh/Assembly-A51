


YJHZ:	MOV	CISHU,#16
	LCALL	XIECX
	RET

YJSZ:	MOV	CISHU,#8
	MOV	ZS,#1
	MOV	B,#16
	MUL	AB
	MOV	TB1,A
	MOV	DPTR,#TABSZ
	LCALL	XIECX
	RET

QP:	SETB	CS1
	SETB	CS2
	MOV	A,#3FH
	LCALL	XIEZL
	MOV	A,#0C0H
	LCALL	XIEZL
	MOV	XJ,#8
	MOV	XD,#0B8H
	MOV	YD,#40H
	MOV	B,#64
QP1:	LCALL	XIE
	MOV	CISHU,B
QP2:	MOV	A,#0
	LCALL	XIESJ
	DJNZ	CISHU,QP2
	INC	XD
	DJNZ	XJ,QP1
	RET

XIECX:	SETB	CS1
	CLR	CS2
	LCALL	TU0
	MOV	A,#0C0H
	LCALL	XIEZL
TU1:	MOV	XJ,#2
TU2:	PUSH	YD
	MOV	CSZJ,CISHU
	CLR	21H.0
	LCALL	XIE
TU3:	MOV	A,TB1
	MOVC	A,@A+DPTR
	LCALL	XIESJ
	INC	YD
	INC	DPTR
	LCALL	TU0
	DJNZ	CSZJ,TU3
	INC	XD
	LCALL	PDQF
	POP	YD
	DJNZ	XJ,TU2
	DEC	XD
	DEC	XD
	MOV	A,YD
	ADD	A,CISHU
	MOV	YD,A
	LCALL	TU0
	DJNZ	ZS,TU1
	MOV	TB1,#0
	RET

PDQF:	JNB	21H.0,TUC
	CPL	CS1
	CPL	CS2
	MOV	A,#0C0H
	LCALL	XIEZL
	RET

TU0:	MOV	A,YD
	CLR	C
	SUBB	A,#80H
	JC	TUC
	ADD	A,#40H
	MOV	YD,A
	SETB	21H.0
	LCALL	PDQF
	LCALL	XIE
TUC:	RET

XIE:	MOV	A,XD
	LCALL	XIEZL
	MOV	A,YD
	LCALL	XIEZL
	RET

XIEZL:	CLR	E
	CLR	RS
	MOV	P0,A
	SETB	E
	CLR	E
	RET

XIESJ:	CLR	E
	SETB	RS
	MOV	P0,A
	SETB	E
	CLR	EE
	RET